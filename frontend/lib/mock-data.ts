export const getRandomPriceChange = (basePrice: number) => {
  const change = (Math.random() - 0.5) * 0.1 * basePrice;
  return basePrice + change;
};

export const simulatePriceUpdate = (prices: TokenPrice[]): TokenPrice[] => {
  return prices.map((token) => ({
    ...token,
    price: getRandomPriceChange(token.price),
    change24h: token.change24h + (Math.random() - 0.5) * 2,
  }));
};

